
public class Switch {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// Menu
		int weeknumber=1;
		
		switch(weeknumber){
		case 1: System.out.println("Sun");
		break;
		case 2: System.out.println("Mon");
		break;
		default :
		System.out.println("Tues");
		break;
		}
	}
	}
	
